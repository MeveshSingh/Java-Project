package Tutorial1;

public class Demo3 {
	
	public static void main(String[] args) {
		
		int a=100;
		
		System.out.println(a);
		
		a+=10;	//a=a+10;		110
		
		System.out.println(a);
		
		a-=40;	//a=a-40  (110-40)
		
		System.out.println(a);//70
		
		a/=2;
		
		System.out.println(a);//35
		
		a*=10;
		
		System.out.println(a);//350
	}

}
